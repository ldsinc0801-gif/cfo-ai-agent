/** 全勘定科目のフラットリスト */
export function getAllAccountNames(): string[] {
  return FREEE_ACCOUNT_GROUPS.flatMap(g => g.accounts);
}

/** セレクトボックス用のHTML optionを生成 */
export function accountSelectOptions(selected?: string): string {
  return FREEE_ACCOUNT_GROUPS.map(g =>
    `<optgroup label="${g.label}">${g.accounts.map(a =>
      `<option value="${a}"${a === selected ? ' selected' : ''}>${a}</option>`
    ).join('')}</optgroup>`
  ).join('');
}
